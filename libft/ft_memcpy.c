#include "./libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	const char		*src_cpy;
	unsigned char	*dst_cpy;

	src_cpy = src;
	dst_cpy = dst;
	i = 0;
	while (i < n)
	{
		dst_cpy[i] = src_cpy[i];
		i++;
	}
	return (dst);
}
